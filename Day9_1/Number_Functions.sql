-- Number Functions 

-- abs() : Returns the absolute value

select abs(-12);

-- sqrt() : Returns sqrt value 

select sqrt(49);

-- power(n,m) : returns n power m value

select power(2,3);

-- ceiling() : Returns the greatest integer value 

select ceiling(12.000001);

-- floor() : Returns the smallest integer value 

select floor(12.999999);

-- Round() : used to round number to particular digits specified 

select round(8778.7876,3);
select round(8778.7876,2);
select round(8778.7876,1);
select round(8778.7876,0);
select round(8778.7876,-1);
select round(8778.7876,-2);
select round(8778.7876,-3);





